dia = int(input("Informe um número (1 a 7): "))

if (dia == 1):
    print("1 - Domingo")
elif (dia == 2):
    print("2 - Segunda")
elif (dia == 3):
    print("3 - Terça")
elif (dia == 4):
    print("4 - Quarta-feira")
elif (dia == 5):
    print("5 - Quinta-feira")
elif (dia == 6):
    print("6 - Sexta-feira")
elif (dia == 7): 
    print("7 - Sabádo")
else:
    print("Opção Inválida!")